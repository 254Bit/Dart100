void main() {
// Logic --> If, IfElse and If Else If

// If Statements; Allows us to check for some basic condition: Ex, to confirm passwords are matching
  var num = 3;
  if (num == 5) {
    print('The number is 5!');
  } else if (num == 3) {
    print('The Number is 3!');
  } else {
    print('The number is NOT 5 Nor 3. It is $num');
  }

// If Else Statements
}
